############################################################
# FLAGS                                                     
############################################################

if [[  $@ =~ .*"--help".* ]] || [[ $@ =~ .*"-h".* ]]; then
	
	echo
	Help
	exit

elif [[  $@ =~ .*"--version".* ]] || [[ $@ =~ .*"-v".* ]]; then
	
	echo
	Version
	exit

fi


############################################################
# OPTIONS                                                     
############################################################


# cache                                                     
############################################################

if [[ "$1" == "cache" ]]; then

	cache "$@"

# cache with args                                                     
############################################################

elif [[ "$1" == "cache="* ]]; then

	cache_arg "$@"

# CLOSE                                                     
############################################################

elif [[ "$1" == "close" ]]; then

	close "$@"

# CLOSE with args                                                     
############################################################

elif [[ "$1" == "close="* ]]; then

	close_arg "$@"

# APPS                                                   
############################################################

elif [[ "$1" == "app" ]]; then

	apps "$@"


# open                                                    
############################################################

elif [[ "$1" == "open" ]]; then

	open_funct "$@"

# workflows                                                    
############################################################

elif [[ "$1" == "workflow" ]]; then

	workflow "$@"

# SPOTLIGHT                                                    
############################################################

elif [[ "$1" == "spotlight" ]]; then

	spotlight "$@"

# terminal                                                    
############################################################

elif [[ "$1" == "terminal" ]] || [[ "$1" == "term" ]]; then

	terminal_funct "$@"

# terminal                                                    
############################################################

elif [[ "$1" == "notification" ]] || [[ "$1" == "not" ]]; then

	not_funct "$@"

# DESK PICS                                                    
############################################################

elif [[ "$1" == "desk-pics" ]]; then

	desk_pics "$@"

# Else                                                    
############################################################

else 
	
	if [[ "$1" == "" ]]; then

		echo
		Help
		exit

	else 
		echo "‚ùå Does not exist cmd option $1"
	fi

fi

